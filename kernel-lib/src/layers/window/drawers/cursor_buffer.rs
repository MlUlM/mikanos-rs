use alloc::vec::Vec;
use core::iter::repeat;

use common_lib::math::vector::Vector2D;

const CURSOR_WIDTH: usize = 15;

const CURSOR_HEIGHT: usize = 24;


const CURSOR_SHAPE: [&[u8; CURSOR_WIDTH]; CURSOR_HEIGHT] = [
    b"@              ",
    b"@@             ",
    b"@.@            ",
    b"@..@           ",
    b"@...@          ",
    b"@....@         ",
    b"@.....@        ",
    b"@......@       ",
    b"@.......@      ",
    b"@........@     ",
    b"@.........@    ",
    b"@..........@   ",
    b"@...........@  ",
    b"@............@ ",
    b"@......@@@@@@@@",
    b"@......@       ",
    b"@....@@.@      ",
    b"@...@ @.@      ",
    b"@..@   @.@     ",
    b"@.@    @.@     ",
    b"@@      @.@    ",
    b"@       @.@    ",
    b"         @.@   ",
    b"         @@@   ",
];

#[derive(Debug)]
#[repr(transparent)]
pub struct CursorBuffer(Vec<Vec<u8>>);


impl CursorBuffer {
    pub fn new(scale: Vector2D<usize>) -> CursorBuffer {
        let cursors: Vec<Vec<u8>> = CURSOR_SHAPE
            .map(|row| {
                row.iter()
                    .flat_map(|p| repeat(*p).take(scale.x()))
                    .collect::<Vec<u8>>()
            })
            .into_iter()
            .flat_map(|row| repeat(row).take(scale.y()))
            .collect();

        CursorBuffer(cursors)
    }


    pub fn no_scale() -> CursorBuffer {
        Self::new(Vector2D::unit())
    }
}


impl Default for CursorBuffer {
    fn default() -> Self {
        Self::no_scale()
    }
}


